
public class Yemekler {
	private String ad;
	private String malzeme;

	public Yemekler(String ad, String malzeme) {
		super();
		this.ad = ad;
		this.malzeme = malzeme;
	}

	public String getAd() {
		return ad;
	}

	public void setAd(String ad) {
		this.ad = ad;
	}

	public String getMalzeme() {
		return malzeme;
	}

	public void setMalzeme(String malzeme) {
		this.malzeme = malzeme;
	}
	
}
